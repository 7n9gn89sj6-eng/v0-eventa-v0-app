/**
 * True when {@param segment} is a plausible Mapbox country label (not a street or suburb).
 */
export function isKnownMapboxCountryName(segment: string): boolean {
  const t = segment?.trim()
  if (!t) return false
  const key = normKey(t)
  if (key.length < 2) return false
  // Numeric tails are postcodes, not countries
  if (/^\d{3,}$/.test(key.replace(/\s/g, ""))) return false
  return KNOWN_MAPBOX_COUNTRY_KEYS.has(key)
}

/**
 * Last comma segment of {@param placeName} only when it looks like a real country
 * (requires ≥2 segments so single-token queries do not become "country = street").
 */
export function mapboxCountryFromPlaceNameTailIfKnown(placeName: string | undefined | null): string | null {
  if (!placeName?.trim()) return null
  const parts = placeName.split(",").map((p) => p.trim()).filter(Boolean)
  if (parts.length < 2) return null
  const tail = parts[parts.length - 1]!
  if (!isKnownMapboxCountryName(tail)) return null
  return tail
}
