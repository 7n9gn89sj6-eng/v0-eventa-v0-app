/**
 * Mapbox {@link feature.place_name} tails and context country names we treat as reliable.
 * Used only to avoid inventing a country from an arbitrary last comma segment (e.g. a street).
 */

function normKey(s: string): string {
  return s
    .trim()
    .toLowerCase()
    .replace(/\.+/g, "")
    .replace(/\s+/g, " ")
}

